// Global variables for screen metrics and mapping
int width = 1024;
int height = 768;
int line_len = 1024; // in pixels

static int fbfd = -1;
static uint32_t *fb_mem = NULL;
static uint32_t *back_buffer = NULL;
static uint32_t *cached_background = NULL;
static long int screensize = 0;
static int bytes_per_pixel = 4;

int init_graphics() {
    printf("[-] Initializing Graphics system...\n");

    fbfd = open("/dev/fb0", O_RDWR);
    if (fbfd < 0) {
        perror("[!] Error: Cannot open framebuffer device");
        return -1;
    }

    struct fb_var_screeninfo vinfo;
    struct fb_fix_screeninfo finfo;

    if (ioctl(fbfd, FBIOGET_VSCREENINFO, &vinfo) < 0) {
        perror("[!] Error: Reading variable screen info");
        close(fbfd);
        return -1;
    }
    if (ioctl(fbfd, FBIOGET_FSCREENINFO, &finfo) < 0) {
        perror("[!] Error: Reading fixed screen info");
        close(fbfd);
        return -1;
    }

    width = vinfo.xres;
    height = vinfo.yres;
    bytes_per_pixel = vinfo.bits_per_pixel / 8;
    if (bytes_per_pixel < 3 || bytes_per_pixel > 4) {
        bytes_per_pixel = 4;
    }
    line_len = finfo.line_length / bytes_per_pixel;
    screensize = finfo.smem_len;

    fb_mem = (uint32_t *)mmap(0, screensize, PROT_READ | PROT_WRITE, MAP_SHARED, fbfd, 0);
    if (fb_mem == MAP_FAILED) {
        perror("[!] Error: Failed to memory-map framebuffer");
        close(fbfd);
        return -1;
    }

    back_buffer = (uint32_t *)malloc(line_len * height * sizeof(uint32_t));
    if (!back_buffer) {
        perror("[!] Error: Back buffer allocation failed");
        munmap(fb_mem, screensize);
        close(fbfd);
        return -1;
    }

    srand(1337);

    // Pre-allocate and cache the website-themed background (heavy math & film grain noise)
    cached_background = (uint32_t *)malloc(line_len * height * sizeof(uint32_t));
    if (cached_background) {
        int glow_cx = width * 3 / 4;
        int glow_cy = height / 4;
        float max_dist = (float)width;
        for (int y = 0; y < height; y++) {
            for (int x = 0; x < width; x++) {
                int dx = x - glow_cx;
                int dy = y - glow_cy;
                float dist = sqrtf((float)(dx * dx + dy * dy));

                // Soft radial taupe glow (3% opacity max, decaying exponentially)
                float intensity = 0.03f * expf(-dist / (max_dist * 0.40f));
                if (intensity > 1.0f) intensity = 1.0f;

                // Blend dark canvas #0d0d0d with the taupe glow #b7ab98
                uint32_t pixel_color = blend_color(COLOR_BG, COLOR_TAUPE, intensity);
                
                // Apply global 3.5% film-grain noise texture
                int noise = (rand() % 9) - 4; // -4 to +4 color variation
                uint8_t r = ((pixel_color >> 16) & 0xFF);
                uint8_t g = ((pixel_color >> 8) & 0xFF);
                uint8_t b = (pixel_color & 0xFF);
                
                r = (r + noise < 0) ? 0 : (r + noise > 255) ? 255 : r + noise;
                g = (g + noise < 0) ? 0 : (g + noise > 255) ? 255 : g + noise;
                b = (b + noise < 0) ? 0 : (b + noise > 255) ? 255 : b + noise;

                cached_background[y * line_len + x] = (r << 16) | (g << 8) | b;
            }
        }
    }

    return 0;
}

void close_graphics() {
    if (cached_background) {
        free(cached_background);
        cached_background = NULL;
    }
    if (back_buffer) {
        free(back_buffer);
        back_buffer = NULL;
    }
    if (fb_mem && fb_mem != MAP_FAILED) {
        munmap(fb_mem, screensize);
        fb_mem = NULL;
    }
    if (fbfd >= 0) {
        close(fbfd);
        fbfd = -1;
    }
}

void blit_screen() {
    if (!fb_mem || !back_buffer) return;

    if (bytes_per_pixel == 4) {
        memcpy(fb_mem, back_buffer, line_len * height * 4);
    } else if (bytes_per_pixel == 3) {
        uint8_t *dst = (uint8_t *)fb_mem;
        uint32_t *src = back_buffer;
        for (int y = 0; y < height; y++) {
            for (int x = 0; x < width; x++) {
                uint32_t color = src[y * line_len + x];
                uint8_t r = (color >> 16) & 0xFF;
                uint8_t g = (color >> 8) & 0xFF;
                uint8_t b = color & 0xFF;
                
                int dst_idx = y * (line_len * 3) + x * 3;
                dst[dst_idx] = b;
                dst[dst_idx + 1] = g;
                dst[dst_idx + 2] = r;
            }
        }
    }
}

void clear_screen() {
    if (back_buffer) {
        memset(back_buffer, 0, line_len * height * sizeof(uint32_t));
    }
}


void put_pixel(int x, int y, uint32_t color) {
    if (x >= 0 && x < width && y >= 0 && y < height && back_buffer) {
        back_buffer[y * line_len + x] = color;
    }
}

uint32_t get_pixel(int x, int y) {
    if (x >= 0 && x < width && y >= 0 && y < height && back_buffer) {
        return back_buffer[y * line_len + x];
    }
    return 0;
}

uint32_t blend_color(uint32_t base, uint32_t blend, float alpha) {
    if (alpha <= 0.0f) return base;
    if (alpha >= 1.0f) return blend;

    uint8_t r_b = (base >> 16) & 0xFF;
    uint8_t g_b = (base >> 8) & 0xFF;
    uint8_t b_b = base & 0xFF;

    uint8_t r_l = (blend >> 16) & 0xFF;
    uint8_t g_l = (blend >> 8) & 0xFF;
    uint8_t b_l = blend & 0xFF;

    uint8_t r = (uint8_t)(r_b * (1.0f - alpha) + r_l * alpha);
    uint8_t g = (uint8_t)(g_b * (1.0f - alpha) + g_l * alpha);
    uint8_t b = (uint8_t)(b_b * (1.0f - alpha) + b_l * alpha);

    return (r << 16) | (g << 8) | b;
}

void draw_rect(int x1, int y1, int x2, int y2, uint32_t color, float alpha) {
    for (int y = y1; y <= y2; y++) {
        for (int x = x1; x <= x2; x++) {
            if (x >= 0 && x < width && y >= 0 && y < height) {
                if (alpha >= 1.0f) {
                    put_pixel(x, y, color);
                } else {
                    uint32_t base = back_buffer[y * line_len + x];
                    put_pixel(x, y, blend_color(base, color, alpha));
                }
            }
        }
    }
}

void draw_border(int x1, int y1, int x2, int y2, uint32_t color, float opacity) {
    for (int x = x1; x <= x2; x++) {
        if (x >= 0 && x < width) {
            if (y1 >= 0 && y1 < height)
                back_buffer[y1 * line_len + x] = blend_color(back_buffer[y1 * line_len + x], color, opacity);
            if (y2 >= 0 && y2 < height)
                back_buffer[y2 * line_len + x] = blend_color(back_buffer[y2 * line_len + x], color, opacity);
        }
    }
    for (int y = y1; y <= y2; y++) {
        if (y >= 0 && y < height) {
            if (x1 >= 0 && x1 < width)
                back_buffer[y * line_len + x1] = blend_color(back_buffer[y * line_len + x1], color, opacity);
            if (x2 >= 0 && x2 < width)
                back_buffer[y * line_len + x2] = blend_color(back_buffer[y * line_len + x2], color, opacity);
        }
    }
}

void draw_circle(int cx, int cy, int r, uint32_t color, float alpha) {
    for (int y = cy - r; y <= cy + r; y++) {
        for (int x = cx - r; x <= cx + r; x++) {
            if (x >= 0 && x < width && y >= 0 && y < height) {
                int dx = x - cx;
                int dy = y - cy;
                if (dx * dx + dy * dy <= r * r) {
                    if (alpha >= 1.0f) {
                        put_pixel(x, y, color);
                    } else {
                        uint32_t base = back_buffer[y * line_len + x];
                        put_pixel(x, y, blend_color(base, color, alpha));
                    }
                }
            }
        }
    }
}

void draw_circle_outline(int cx, int cy, int r, uint32_t color, float opacity) {
    int x = r;
    int y = 0;
    int err = 0;

    while (x >= y) {
        if (cx + x >= 0 && cx + x < width && cy + y >= 0 && cy + y < height)
            back_buffer[(cy + y) * line_len + (cx + x)] = blend_color(back_buffer[(cy + y) * line_len + (cx + x)], color, opacity);
        if (cx + y >= 0 && cx + y < width && cy + x >= 0 && cy + x < height)
            back_buffer[(cy + x) * line_len + (cx + y)] = blend_color(back_buffer[(cy + x) * line_len + (cx + y)], color, opacity);
        if (cx - y >= 0 && cx - y < width && cy + x >= 0 && cy + x < height)
            back_buffer[(cy + x) * line_len + (cx - y)] = blend_color(back_buffer[(cy + x) * line_len + (cx - y)], color, opacity);
        if (cx - x >= 0 && cx - x < width && cy + y >= 0 && cy + y < height)
            back_buffer[(cy + y) * line_len + (cx - x)] = blend_color(back_buffer[(cy + y) * line_len + (cx - x)], color, opacity);
        if (cx - x >= 0 && cx - x < width && cy - y >= 0 && cy - y < height)
            back_buffer[(cy - y) * line_len + (cx - x)] = blend_color(back_buffer[(cy - y) * line_len + (cx - x)], color, opacity);
        if (cx - y >= 0 && cx - y < width && cy - x >= 0 && cy - x < height)
            back_buffer[(cy - x) * line_len + (cx - y)] = blend_color(back_buffer[(cy - x) * line_len + (cx - y)], color, opacity);
        if (cx + y >= 0 && cx + y < width && cy - x >= 0 && cy - x < height)
            back_buffer[(cy - x) * line_len + (cx + y)] = blend_color(back_buffer[(cy - x) * line_len + (cx + y)], color, opacity);
        if (cx + x >= 0 && cx + x < width && cy - y >= 0 && cy - y < height)
            back_buffer[(cy - y) * line_len + (cx + x)] = blend_color(back_buffer[(cy - y) * line_len + (cx + x)], color, opacity);

        y += 1;
        if (err <= 0) {
            err += 2 * y + 1;
        }
        if (err > 0) {
            x -= 1;
            err -= 2 * x + 1;
        }
    }
}

void draw_char(int x, int y, char c, uint32_t color) {
    if (c < 32 || c > 126) return;
    int idx = c - 32;
    for (int r = 0; r < 16; r++) {
        uint8_t bits = font8x16[idx][r];
        for (int col = 0; col < 8; col++) {
            if (bits & (0x80 >> col)) {
                put_pixel(x + col, y + r, color);
            }
        }
    }
}

void draw_string(int x, int y, const char *str, uint32_t color) {
    while (*str) {
        draw_char(x, y, *str, color);
        x += 8;
        str++;
    }
}

void draw_string_bold(int x, int y, const char *str, uint32_t color) {
    while (*str) {
        char c = *str;
        if (c >= 32 && c <= 126) {
            int idx = c - 32;
            for (int r = 0; r < 16; r++) {
                uint8_t bits = font8x16[idx][r];
                for (int col = 0; col < 8; col++) {
                    if (bits & (0x80 >> col)) {
                        put_pixel(x + col,     y + r,     color);
                        put_pixel(x + col + 1, y + r,     color);
                        put_pixel(x + col,     y + r + 1, color);
                    }
                }
            }
        }
        x += 9;
        str++;
    }
}

void draw_string_large(int x, int y, const char *str, uint32_t color) {
    while (*str) {
        char c = *str;
        if (c >= 32 && c <= 126) {
            int idx = c - 32;
            for (int r = 0; r < 16; r++) {
                uint8_t bits = font8x16[idx][r];
                for (int col = 0; col < 8; col++) {
                    if (bits & (0x80 >> col)) {
                        put_pixel(x + col * 2,     y + r * 2,     color);
                        put_pixel(x + col * 2 + 1, y + r * 2,     color);
                        put_pixel(x + col * 2,     y + r * 2 + 1, color);
                        put_pixel(x + col * 2 + 1, y + r * 2 + 1, color);
                        put_pixel(x + col * 2 + 2, y + r * 2,     color);
                    }
                }
            }
        }
        x += 18;
        str++;
    }
}

void draw_desktop_background() {
    if (cached_background && back_buffer) {
        // High performance cache copy (instantaneous block copy)
        memcpy(back_buffer, cached_background, line_len * height * sizeof(uint32_t));
    } else {
        // Fallback: dynamic calculation if buffer allocation failed
        int glow_cx = width * 3 / 4;
        int glow_cy = height / 4;
        float max_dist = (float)width;

        for (int y = 0; y < height; y++) {
            for (int x = 0; x < width; x++) {
                int dx = x - glow_cx;
                int dy = y - glow_cy;
                float dist = sqrtf((float)(dx * dx + dy * dy));

                float intensity = 0.03f * expf(-dist / (max_dist * 0.40f));
                if (intensity > 1.0f) intensity = 1.0f;

                uint32_t pixel_color = blend_color(COLOR_BG, COLOR_TAUPE, intensity);
                
                int noise = (rand() % 9) - 4;
                uint8_t r = ((pixel_color >> 16) & 0xFF);
                uint8_t g = ((pixel_color >> 8) & 0xFF);
                uint8_t b = (pixel_color & 0xFF);
                
                r = (r + noise < 0) ? 0 : (r + noise > 255) ? 255 : r + noise;
                g = (g + noise < 0) ? 0 : (g + noise > 255) ? 255 : g + noise;
                b = (b + noise < 0) ? 0 : (b + noise > 255) ? 255 : b + noise;

                back_buffer[y * line_len + x] = (r << 16) | (g << 8) | b;
            }
        }
    }
}
